## NOTE: 3+ qubit custom operations are lowered through the recursive Quantum
#        Shannon Decomposition (arXiv quant-ph/0406176), which uses the existing
#        2-qubit KAK and 1-qubit ZYZ decomposers as base cases. The random
#        operations below are generated with `scipy.stats.unitary_group.rvs`
#        using `seed=13` so the matrices are reproducible.


def nearest_unitary(matrix):
    # The matrices below are transcribed from `scipy.stats.unitary_group.rvs`
    # with only 8 decimal digits, so they are unitary to ~1e-8. Project them
    # back onto the nearest unitary (polar factor via SVD) so the registered
    # operator is exactly unitary, matching what a user would obtain directly
    # from `unitary_group.rvs`. Toffoli is an exact permutation and untouched.
    u, _, vh = np.linalg.svd(matrix)
    return u @ vh


def check_state(matrix, state):
    # The state produced from |0...0> must match the first column of the custom
    # unitary matrix.
    assert np.isclose(matrix[:, 0], np.array(state), atol=1e-8).all()


