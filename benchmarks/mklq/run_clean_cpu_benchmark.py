"""Run the MKL-Q clean CPU benchmark gate end-to-end."""

import argparse
import json
import os
import subprocess
import sys
from dataclasses import dataclass
from datetime import date
from pathlib import Path
from typing import Any


DEFAULT_GATE_CASES = "y-state,cy-state,cz-state"
DEFAULT_SAMPLING_CASES = "sample-full-register,sample-partial-register"
DEFAULT_TARGETS = "qpp-cpu,mklq-cpu"
DEFAULT_PERFORMANCE_SCOPE = (
    "local Apple M5 q20 CPU target comparison only; not a cross-machine "
    "release benchmark")
DEFAULT_SUMMARY_TEXT = (
    "Clean-worktree local run comparing qpp-cpu and mklq-cpu for q20 Y/CY/CZ "
    "state updates plus full/partial-register sampling at 1024 and 65536 "
    "shots.")
DEFAULT_RUNTIME_NOTE = (
    "The CUDA-Q Python runtime and source provenance are recorded from the raw "
    "benchmark reports generated by run_clean_cpu_benchmark.py.")


@dataclass(frozen=True)
class GateConfig:
    repo_root: Path
    pythonpath: str
    stamp: str
    qubits: int
    threads: int
    repeats: int
    warmups: int
    layers: int
    shots: int
    shot_counts: str
    results_dir: Path
    reports_dir: Path
    evidence_output: Path
    targets: str
    gate_cases: str
    sampling_cases: str
    summary_id: str
    evidence_kind: str
    ratio_group: str
    performance_scope: str
    summary_text: str
    runtime_note: str
    allow_dirty: bool
    skip_benchmark: bool
    refresh_evidence: bool


def repo_root() -> Path:
    return Path(__file__).resolve().parents[2]


def resolve_path(root: Path, path: Path) -> Path:
    return path if path.is_absolute() else root / path


def positive_int(value: str) -> int:
    try:
        parsed = int(value)
    except ValueError as exc:
        raise argparse.ArgumentTypeError(f"invalid integer '{value}'") from exc
    if parsed < 1:
        raise argparse.ArgumentTypeError(f"expected positive integer, got {parsed}")
    return parsed


def fixed_env(config: GateConfig) -> dict[str, str]:
    return {
        "OMP_NUM_THREADS": str(config.threads),
        "OMP_PROC_BIND": "close",
        "OMP_DYNAMIC": "false",
        "VECLIB_MAXIMUM_THREADS": "1",
        "PYTHONPATH": config.pythonpath,
    }


def output_paths(config: GateConfig) -> dict[str, Path]:
    q_label = f"q{config.qubits}"
    gate_label = case_label(config.gate_cases)
    return {
        "gate_raw": config.results_dir /
        f"local-clean-cpu-gate-{gate_label}-{q_label}-{config.stamp}.json",
        "sampling_raw": config.results_dir /
        f"local-clean-cpu-sampling-{q_label}-{config.stamp}.json",
        "summary": config.reports_dir / f"{config.summary_id}.summary.json",
        "evidence": config.evidence_output,
    }


def case_label(cases: str) -> str:
    labels: list[str] = []
    for case in cases.split(","):
        label = case.strip()
        if not label:
            continue
        if label.endswith("-state"):
            label = label[:-len("-state")]
        labels.append(label.replace("_", "-"))
    return "-".join(labels) if labels else "gate"


def command_path(config: GateConfig, path: Path) -> str:
    try:
        return path.relative_to(config.repo_root).as_posix()
    except ValueError:
        return path.as_posix()


def build_plan(config: GateConfig) -> dict[str, Any]:
    paths = output_paths(config)
    bench_script = config.repo_root / "benchmarks" / "mklq" / "bench_mklq_targets.py"
    make_summary = config.repo_root / "benchmarks" / "mklq" / "make_summary.py"
    summarize_reports = (
        config.repo_root / "benchmarks" / "mklq" / "summarize_reports.py")

    gate_command = [
        sys.executable,
        str(bench_script),
        "--isolate-rows",
        "--targets",
        config.targets,
        "--cases",
        config.gate_cases,
        "--qubits",
        str(config.qubits),
        "--shots",
        str(config.shots),
        "--repeats",
        str(config.repeats),
        "--warmups",
        str(config.warmups),
        "--layers",
        str(config.layers),
        "--output",
        command_path(config, paths["gate_raw"]),
    ]
    sampling_command = [
        sys.executable,
        str(bench_script),
        "--isolate-rows",
        "--targets",
        config.targets,
        "--cases",
        config.sampling_cases,
        "--qubits",
        str(config.qubits),
        "--shot-counts",
        config.shot_counts,
        "--repeats",
        str(config.repeats),
        "--warmups",
        str(config.warmups),
        "--layers",
        str(config.layers),
        "--output",
        command_path(config, paths["sampling_raw"]),
    ]
    summary_command = [
        sys.executable,
        str(make_summary),
        "--raw",
        command_path(config, paths["gate_raw"]),
        "--raw",
        command_path(config, paths["sampling_raw"]),
        "--summary-id",
        config.summary_id,
        "--evidence-kind",
        config.evidence_kind,
        "--ratio-group",
        config.ratio_group,
        "--performance-scope",
        config.performance_scope,
        "--summary-text",
        config.summary_text,
        "--runtime-note",
        config.runtime_note,
        "--output",
        command_path(config, paths["summary"]),
    ]
    if config.allow_dirty:
        summary_command.append("--allow-dirty")

    evidence_command = [
        sys.executable,
        str(summarize_reports),
        "--reports",
        command_path(config, config.reports_dir),
        "--format",
        "markdown",
        "--output",
        command_path(config, paths["evidence"]),
    ]

    return {
        "environment": fixed_env(config),
        "paths": {key: value.as_posix() for key, value in paths.items()},
        "commands": {
            "gate_raw": gate_command,
            "sampling_raw": sampling_command,
            "summary": summary_command,
            "evidence": evidence_command,
        },
    }


def git_status_short(root: Path) -> str:
    return subprocess.check_output(["git", "status", "--short"],
                                   cwd=root,
                                   text=True).strip()


def ensure_clean_worktree(config: GateConfig) -> None:
    if config.allow_dirty or config.skip_benchmark:
        return
    status = git_status_short(config.repo_root)
    if status:
        raise SystemExit(
            "working tree is dirty; commit or stash changes before collecting "
            "clean benchmark evidence, or pass --allow-dirty for explicit "
            "tuning evidence")


def run_command(command: list[str], env_overlay: dict[str, str],
                cwd: Path) -> None:
    env = os.environ.copy()
    env.update(env_overlay)
    subprocess.run(command, cwd=cwd, env=env, check=True)


def run_gate(config: GateConfig, plan_only: bool) -> dict[str, Any]:
    plan = build_plan(config)
    if plan_only:
        return plan

    ensure_clean_worktree(config)
    env_overlay = {key: str(value) for key, value in plan["environment"].items()}
    commands = plan["commands"]

    if not config.skip_benchmark:
        run_command(commands["gate_raw"], env_overlay, config.repo_root)
        run_command(commands["sampling_raw"], env_overlay, config.repo_root)

    run_command(commands["summary"], env_overlay, config.repo_root)
    if config.refresh_evidence:
        run_command(commands["evidence"], env_overlay, config.repo_root)
    return plan


def make_config(args: argparse.Namespace) -> GateConfig:
    root = repo_root()
    stamp = args.stamp or date.today().isoformat()
    summary_id = args.summary_id or f"local-clean-cpu-q{args.qubits}-{stamp}"
    return GateConfig(
        repo_root=root,
        pythonpath=args.pythonpath,
        stamp=stamp,
        qubits=args.qubits,
        threads=args.threads,
        repeats=args.repeats,
        warmups=args.warmups,
        layers=args.layers,
        shots=args.shots,
        shot_counts=args.shot_counts,
        results_dir=resolve_path(root, args.results_dir),
        reports_dir=resolve_path(root, args.reports_dir),
        evidence_output=resolve_path(root, args.evidence_output),
        targets=args.targets,
        gate_cases=args.gate_cases,
        sampling_cases=args.sampling_cases,
        summary_id=summary_id,
        evidence_kind=args.evidence_kind,
        ratio_group=args.ratio_group,
        performance_scope=args.performance_scope,
        summary_text=args.summary_text,
        runtime_note=args.runtime_note,
        allow_dirty=args.allow_dirty,
        skip_benchmark=args.skip_benchmark,
        refresh_evidence=not args.no_refresh_evidence,
    )


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Run the MKL-Q clean CPU benchmark evidence gate.")
    parser.add_argument("--pythonpath",
                        default=str(Path.home() / ".cudaq-mklq"),
                        help="PYTHONPATH for the installed CUDA-Q/MKL-Q tree.")
    parser.add_argument("--stamp",
                        help="Date or label for output filenames. Defaults to today.")
    parser.add_argument("--qubits",
                        type=positive_int,
                        default=20,
                        help="Qubit count.")
    parser.add_argument("--threads",
                        type=positive_int,
                        default=10,
                        help="OMP_NUM_THREADS value.")
    parser.add_argument("--repeats",
                        type=positive_int,
                        default=2,
                        help="Measured repeats.")
    parser.add_argument("--warmups",
                        type=positive_int,
                        default=1,
                        help="Warmup iterations.")
    parser.add_argument("--layers",
                        type=positive_int,
                        default=8,
                        help="Benchmark layer count.")
    parser.add_argument("--shots",
                        type=positive_int,
                        default=1024,
                        help="Shot count for gate-case rows.")
    parser.add_argument("--shot-counts",
                        default="1024,65536",
                        help="Shot counts for sampling rows.")
    parser.add_argument("--targets",
                        default=DEFAULT_TARGETS,
                        help="Comma-separated benchmark targets.")
    parser.add_argument("--gate-cases",
                        default=DEFAULT_GATE_CASES,
                        help="Comma-separated gate benchmark cases.")
    parser.add_argument("--sampling-cases",
                        default=DEFAULT_SAMPLING_CASES,
                        help="Comma-separated sampling benchmark cases.")
    parser.add_argument("--results-dir",
                        type=Path,
                        default=Path("benchmarks/mklq/results"),
                        help="Raw benchmark output directory.")
    parser.add_argument("--reports-dir",
                        type=Path,
                        default=Path("benchmarks/mklq/reports"),
                        help="Sanitized summary output directory.")
    parser.add_argument("--evidence-output",
                        type=Path,
                        default=Path("docs/mklq/benchmark-evidence.md"),
                        help="Generated public evidence Markdown path.")
    parser.add_argument("--summary-id",
                        help="Summary id. Defaults to local-clean-cpu-q<qubits>-<stamp>.")
    parser.add_argument("--evidence-kind",
                        default="clean_local_benchmark_evidence",
                        help="Summary evidence_kind value.")
    parser.add_argument("--ratio-group",
                        default="clean_worktree_cross_target_ratio",
                        help="Comparison ratio group name.")
    parser.add_argument("--performance-scope",
                        default=DEFAULT_PERFORMANCE_SCOPE,
                        help="Summary performance_claim_scope text.")
    parser.add_argument("--summary-text",
                        default=DEFAULT_SUMMARY_TEXT,
                        help="Summary interpretation text.")
    parser.add_argument("--runtime-note",
                        default=DEFAULT_RUNTIME_NOTE,
                        help="Summary runtime note text.")
    parser.add_argument("--allow-dirty",
                        action="store_true",
                        help="Allow dirty-worktree benchmark evidence.")
    parser.add_argument("--skip-benchmark",
                        action="store_true",
                        help="Skip raw benchmark execution and use existing raw JSON.")
    parser.add_argument("--no-refresh-evidence",
                        action="store_true",
                        help="Do not regenerate docs/mklq/benchmark-evidence.md.")
    parser.add_argument("--plan-only",
                        action="store_true",
                        help="Print the planned commands as JSON without running them.")
    return parser.parse_args()


def main() -> int:
    args = parse_args()
    config = make_config(args)
    plan = run_gate(config, plan_only=args.plan_only)
    if args.plan_only:
        print(json.dumps(plan, indent=2, sort_keys=True))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
